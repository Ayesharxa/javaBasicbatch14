package com.syntax.class107;

public class IncreamentAndDecreament {

	public static void main(String[] args) {

		int num=90;
		num=num+1;

		System.out.println(num);	
			
		//another way to increment variable value by 1 -> ++
		num++;  //+1
		 
		num++;
		System.out.println(num);
		
		//another way to decrement variable value by 1 -> --
				num++;  //+1
				 	
		num--;
		System.out.println(num);
			
		int i=10;
		i--;
		i--;
		System.out.println(i);
		
		
		
	}

}
